#include "Zombie.hpp"

Zombie* zombieHorde(int N, std::string name)
{
	int     i;
	Zombie *zombie;

	i = 0;
	zombie = new Zombie[N];
	if (N <= 0 || name.empty())
		return (NULL);
	while (i < N)
	{
		zombie[i] = Zombie(name);
		i++;
	}
	return (zombie);
}
